package com.quentin.startassistws.objetoagencia;

public class Pais {

	private Integer id_pais;
	private String nombre;
	private String continente;
	private String codigo_postal;
	private String code_image;
	
	private String group_continente;
	private String code_nombre_pais;
	public Integer getId_pais() {
		return id_pais;
	}
	public void setId_pais(Integer id_pais) {
		this.id_pais = id_pais;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getContinente() {
		return continente;
	}
	public void setContinente(String continente) {
		this.continente = continente;
	}
	public String getCodigo_postal() {
		return codigo_postal;
	}
	public void setCodigo_postal(String codigo_postal) {
		this.codigo_postal = codigo_postal;
	}
	public String getCode_image() {
		return code_image;
	}
	public void setCode_image(String code_image) {
		this.code_image = code_image;
	}
	public String getGroup_continente() {
		return group_continente;
	}
	public void setGroup_continente(String group_continente) {
		this.group_continente = group_continente;
	}
	public String getCode_nombre_pais() {
		return code_nombre_pais;
	}
	public void setCode_nombre_pais(String code_nombre_pais) {
		this.code_nombre_pais = code_nombre_pais;
	}
	
	
	
}
